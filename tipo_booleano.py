"""
Tipo booleano

Álgebra Booleana, criada por George Boole

No Python a inicial tem que ser sempre maiúscula: "True" ou "False"
"""
ativo = True
logado = False
print(ativo)
print(type(ativo))

"""
Operações básicas
"""

# Negação (not)
print(not ativo)

# Ou (or)
print(ativo or logado)

# E (and)
print(ativo and logado)
